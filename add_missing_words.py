conn = sqlite3.connect("nihongo.db")
conn.row_factory = sqlite3.Row

added = 0
skipped = 0
for word, reading, translation, jlpt, wtype, vgroup in WORDS:
    # Check if already exists (by word OR reading exact match)
    row = conn.execute(
        "SELECT id FROM words WHERE word=? OR reading=?", (word, reading)
    ).fetchone()
    if row:
        skipped += 1
        continue
    conn.execute(
        """INSERT INTO words (word, reading, translation, jlpt_level, word_type, verb_group)
           VALUES (?, ?, ?, ?, ?, ?)""",
        (word, reading, translation, jlpt, wtype, vgroup)
    )
    print(f"  ADD  {word} ({reading}) — {translation}")
    added += 1

conn.commit()
conn.close()
print(f"\nDone: {added} added, {skipped} skipped (already existed).")
